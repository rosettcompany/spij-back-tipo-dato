package pe.gob.minjus.spij.back.tipo.dato.entity;

public class SectorHijoActualizar {
	  public Integer grupo;
	  public String padre_nombre;
	  public String nombreAnterior;
	  public String nombreNuevo;
	  
	public Integer getGrupo() {
		return grupo;
	}
	public void setGrupo(Integer grupo) {
		this.grupo = grupo;
	}
	public String getNombreAnterior() {
		return nombreAnterior;
	}
	public void setNombreAnterior(String nombreAnterior) {
		this.nombreAnterior = nombreAnterior;
	}
	public String getNombreNuevo() {
		return nombreNuevo;
	}
	public void setNombreNuevo(String nombreNuevo) {
		this.nombreNuevo = nombreNuevo;
	}
	public String getPadre_nombre() {
		return padre_nombre;
	}
	public void setPadre_nombre(String padre_nombre) {
		this.padre_nombre = padre_nombre;
	}
	  
}
